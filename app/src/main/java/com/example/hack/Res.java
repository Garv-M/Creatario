package com.example.hack;

public class Res {

    String text;
    int s;

    public Res (int s, String text){
        this.s = s;
        this.text = text;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
